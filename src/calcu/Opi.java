

package calcu;

public class Opi {

    float res;

    public float Sumar(float num1, float num2) {

        res = num1 + num2;
        return res;
    }

    public float Restar(float num1, float num2) {

        res = num1 - num2;
        return res;
    }

    public float Multiplicar(float num1, float num2) {

        res = num1 * num2;
        return res;
    }

    public float Divi(float num1, float num2) {
        float ress;
        ress = num1 / num2;
        return ress;
    }
}
